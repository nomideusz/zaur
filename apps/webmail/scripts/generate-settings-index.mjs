import fs from 'fs';

const BASIC_TITLE_OVERRIDES = new Set([
	'Drawer width',
	'Reset all display & layout settings',
	'Reset look & feel',
	'Reset inbox settings',
	'Reset reading settings',
	'Reset writing settings',
	'Reset layout settings',
	'Reset calendar settings',
	'Reset contacts settings',
	'Reset you settings',
	'Restore mail defaults',
	'Export settings',
	'Import settings'
]);

function slug(title) {
	return title
		.toLowerCase()
		.replace(/[^a-z0-9]+/g, '-')
		.replace(/^-|-$/g, '');
}

function isAdvancedSettingTitle(title) {
	if (BASIC_TITLE_OVERRIDES.has(title)) return false;
	const lower = title.toLowerCase();
	return (
		lower.startsWith('compact ') ||
		lower.startsWith('hide ') ||
		lower.startsWith('minimal ') ||
		lower.includes('icon-only') ||
		lower.includes('skeleton') ||
		lower === 'hide connecting screen'
	);
}

function shouldShowInBasic({ advanced, title }) {
	if (advanced) return false;
	if (title) return !isAdvancedSettingTitle(title);
	return true;
}

function parseGroups(source) {
	const groups = [];
	const groupRe = /<SettingsGroup([^>]*)>([\s\S]*?)<\/SettingsGroup>/g;
	let match;
	while ((match = groupRe.exec(source)) !== null) {
		const attrs = match[1];
		const body = match[2];
		const explicit = attrs.match(/advanced=\{?(true|false)\}?/);
		const advanced = explicit ? explicit[1] === 'true' : /\badvanced\b/.test(attrs);
		const rows = [];
		const rowRe = /<(SettingsRow|SettingsField)[^>]*title="([^"]+)"/g;
		let rowMatch;
		while ((rowMatch = rowRe.exec(body)) !== null) {
			rows.push(rowMatch[2]);
		}
		groups.push({ advanced, rows });
	}
	return groups;
}

function countHiddenForSource(source) {
	let hidden = 0;
	for (const group of parseGroups(source)) {
		for (const title of group.rows) {
			if (!shouldShowInBasic({ advanced: group.advanced, title })) {
				hidden++;
			}
		}
	}
	return hidden;
}

const files = [
	['src/lib/settings/sections/appearance.svelte', '/settings/appearance'],
	['src/lib/settings/sections/reading-layout.svelte', '/settings/reading'],
	['src/lib/settings/sections/reading-core.svelte', '/settings/reading'],
	['src/lib/settings/sections/writing.svelte', '/settings/writing'],
	['src/lib/settings/sections/data_reset.svelte', '/settings/data'],
	['src/lib/settings/sections/data_backup.svelte', '/settings/data'],
	['src/routes/(app)/settings/account/+page.svelte', '/settings/account']
];

const entries = [];
const seen = new Set();
const hiddenCounts = {};

for (const [file, href] of files) {
	const src = fs.readFileSync(file, 'utf8');
	const pageHref = href;
	hiddenCounts[pageHref] = (hiddenCounts[pageHref] ?? 0) + countHiddenForSource(src);

	const rowRe = /<(SettingsRow|SettingsField)\s+[^>]*title="([^"]+)"[^>]*(?:\s+description="([^"]*)")?/g;
	let m;
	while ((m = rowRe.exec(src)) !== null) {
		const title = m[2];
		const description = m[3] ?? '';
		const publicHref = href;
		const id = `${publicHref}-${slug(title)}`;
		if (seen.has(id)) continue;
		seen.add(id);
		entries.push({ id, href: publicHref, title, description });
	}
}

entries.sort((a, b) => a.id.localeCompare(b.id));

const searchIndex = `export type SettingsSearchEntry = {
	id: string;
	title: string;
	description: string;
	href: string;
};

export const SETTINGS_SEARCH_INDEX: SettingsSearchEntry[] = ${JSON.stringify(entries, null, '\t')};
`;

const pageStats = `/** Auto-generated by scripts/generate-settings-index.mjs — do not edit manually. */

export const SETTINGS_PAGE_HIDDEN_COUNTS: Record<string, number> = ${JSON.stringify(hiddenCounts, null, '\t')};

export function hiddenSettingsCountForPage(pathname: string): number {
	return SETTINGS_PAGE_HIDDEN_COUNTS[pathname] ?? 0;
}
`;

fs.writeFileSync('src/lib/settings/search-index.ts', searchIndex);
fs.writeFileSync('src/lib/settings/page-stats.ts', pageStats);
console.log(`Wrote ${entries.length} search entries and ${Object.keys(hiddenCounts).length} page stats`);
